    <h4 class="h4_top">Ce projet consiste à créer une carte visuelle sur Adobe InDesign, mettant en avant trois grandes familles de typographie : les manuales, les mécanes et les linéales</h4>
    <p id="paragraphe">
        <br>

        L’objectif principal est de représenter visuellement les caractéristiques de ces familles, tout en apprenant à les différencier et à comprendre leurs usages.
        <br>
        La carte doit inclure plusieurs éléments pour chaque famille. D’abord, le nom de la famille doit être clairement indiqué. Ensuite, il est important d’ajouter des exemples de polices connues appartenant à cette catégorie, ainsi qu’une description des caractéristiques visuelles propres à chaque famille. Cela peut concerner, par exemple, la présence ou l’absence d’empattements, le contraste entre les pleins et les déliés, ou encore le style global (plutôt géométrique, naturel, rigide ou fluide). Il est également possible d’ajouter un petit contexte historique ou des usages typiques de ces polices (comme dans la presse, les titres, les interfaces numériques, etc.).
        <br>
         Réalisations <br>
         Le projet se réalise sur InDesign, un logiciel professionnel de mise en page. Cela permet de travailler la disposition des éléments, l’équilibre graphique, la hiérarchisation de l’information, ainsi que la cohérence visuelle entre les différentes sections de la carte. Chaque famille peut être présentée dans une zone ou colonne bien distincte, afin de faciliter la lecture et la comparaison.
        <br>
        </p>
