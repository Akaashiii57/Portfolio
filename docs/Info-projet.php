             Objectifs <br>
            - Moderniser l’identité visuelle pour refléter les valeurs de solidarité. <br>
            - Créer une charte graphique cohérente avec un logo, une palette de couleurs et une typographie adaptées. <br>
            - Repenser le site internet pour une navigation intuitive et un accès facilité aux informations. <br>
            - Améliorer l’expérience utilisateur (UX/UI) et optimiser le site pour le référencement (SEO).
            <br>
             Réalisations <br>
            - Conception d’un logo et d’une charte graphique moderne et harmonieuse. <br>
            - Développement de supports de communication visuelle (flyers, affiches, bannières). <br>
            - Refonte du site avec une interface ergonomique, responsive et optimisée. <br> 
            - Intégration de nouvelles fonctionnalités : formulaire de contact, espace bénévoles, actualités. 
            <br>
             Résultats <br>
            Cette refonte a renforcé l’image de l’association et amélioré l’expérience des visiteurs, facilitant l’accès aux services et l’engagement des bénévoles et donateurs.
